print('Calcule a area do trapezio, com base nos valores abaixo: \n')

B = float(input('Digite o valor da base maior: '))
b = float(input('Digite o valor da base menor: '))
h = float(input('Digite o valor da altura: '))

A = ((B + b) * h) / 2

print(f'O valor da area do trapezio eh igual a {A :.2f} m²')